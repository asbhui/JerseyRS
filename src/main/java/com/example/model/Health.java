package com.example.model;

public class Health {

	private String status;
	
	public Health(String status) {
		this.setStatus(status);
	}

	/**
	 * @return the status
	 */
	public String getStatus() {
		return status;
	}

	/**
	 * @param status the status to set
	 */
	public void setStatus(String status) {
		this.status = status;
	}
	
	
	
	
}
